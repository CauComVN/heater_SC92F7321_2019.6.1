/**
* 简述：根据ModBus规则计算CRC16
* 参数：_pBuf:待计算数据缓冲区, _usLen:待计算数据长度(字节数)
* 返回：16位校验值
*/
uint16_t getModbusCRC16(uint8_t *_pBuf, uint16_t _usLen)
{
	uint16_t CRCValue = 0xFFFF;  					//初始化CRC变量各位为1
	uint8_t i,j;
	for(i=0;i<_usLen;++i)
	{
		CRCValue  ^= *(_pBuf+i);       			    //当前数据异或CRC低字节
		for(j=0;j<8;++j)                            //一个字节重复右移8次
		{
			if((CRCValue & 0x01) == 0x01)           //判断右移前最低位是否为1
			{
				 CRCValue = (CRCValue >> 1)^0xA001; //如果为1则右移并异或表达式
			}else 
			{
				CRCValue >>= 1;                     //否则直接右移一位
			}			
		}

	} 
	
	return 	CRCValue<<8 | (0xFF00&CRCValue)>>8;     //CRCValue寄存器高低字节调换并返回
} 

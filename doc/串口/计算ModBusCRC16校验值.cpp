/*
****************************************************************
*****************************************
* 函 数 名: CRC16_Modbus
* 功能说明: 计算 CRC。 用于 Modbus 协议。
* 形 参: _pBuf : 参与校验的数据
* _usLen : 数据长度
* 返 回 值: 16 位整数值。 对于 Modbus ， 此结果高字节先传送， 低
字节后传送。
** 所有可能的 CRC 值都被预装在两个数组当中， 当计算报文内容时可
以简单的索引即可；
* 一个数组包含有 16 位 CRC 域的所有 256 个可能的高位字节， 另一个
数组含有低位字节的值；
* 这种索引访问 CRC 的方式提供了比对报文缓冲区的每一个新字符都
计算新的 CRC 更快的方法；
* *
注意： 此程序内部执行高/低 CRC 字节的交换。 此函数返回的是已经
经过交换的 CRC 值； 也就是说， 该函数的返回值可以直接放置
* 于报文用于发送；
****************************************************************
*****************************************
*/
uint16_t CRC16_Modbus(uint8_t *_pBuf, uint16_t _usLen)
{
	uint8_t ucCRCHi = 0xFF; /* 高 CRC 字节初始化 */
	uint8_t ucCRCLo = 0xFF; /* 低 CRC 字节初始化 */
	uint16_t usIndex; /* CRC 循环中的索引 */
	while (_usLen--)
	{
		usIndex = ucCRCHi ^ *_pBuf++; /* 计算 CRC */
		ucCRCHi = ucCRCLo ^ s_CRCHi[usIndex];
		ucCRCLo = s_CRCLo[usIndex];
	} 
	 
	 return ((uint16_t)ucCRCHi << 8 | ucCRCLo);
}

